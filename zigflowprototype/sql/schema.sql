CREATE TABLE restaurant (
    datasetId VARCHAR(50),
    name VARCHAR(100),
    address1 VARCHAR(100),
    address2 VARCHAR(100),
    city VARCHAR(20),
    state VARCHAR(5),
    zip VARCHAR(20),
    tel VARCHAR(20),
    start_date VARCHAR(50),
    type VARCHAR(100),
    geoX VARCHAR(50),
    geoY VARCHAR(50),
    county VARCHAR(10) NOT NULL,
    end_date VARCHAR(50),
    id VARCHAR(100)
);
